import java.util.*;
import java.io.*;
public class AlternateNumbers
{
    public static void main(String[]args)
    {
        Scanner s=new Scanner(System.in);
        for(int i=1;i<=99;i++)
        {
            System.out.print(i);
            System.out.print(" ");
            i=i+1;
        }
    }

}